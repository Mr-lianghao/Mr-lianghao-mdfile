package com.citybus.domain;

public class Things {

	private Integer ID;
	private String thing_name;
	private String thing_date;
	private String thing_detail;
	private String thing_people;
	private String thing_peoplePhone;
	private String thing_flag;
	private String thing_routeNum;
	private Integer state;
	public Integer getID() {
		return ID;
	}
	public void setID(Integer iD) {
		ID = iD;
	}
	public String getThing_name() {
		return thing_name;
	}
	public void setThing_name(String thing_name) {
		this.thing_name = thing_name;
	}
	public String getThing_date() {
		return thing_date;
	}
	public void setThing_date(String thing_date) {
		this.thing_date = thing_date;
	}
	public String getThing_detail() {
		return thing_detail;
	}
	public void setThing_detail(String thing_detail) {
		this.thing_detail = thing_detail;
	}
	public String getThing_people() {
		return thing_people;
	}
	public void setThing_people(String thing_people) {
		this.thing_people = thing_people;
	}
	public String getThing_peoplePhone() {
		return thing_peoplePhone;
	}
	public void setThing_peoplePhone(String thing_peoplePhone) {
		this.thing_peoplePhone = thing_peoplePhone;
	}
	public String getThing_flag() {
		return thing_flag;
	}
	public void setThing_flag(String thing_flag) {
		this.thing_flag = thing_flag;
	}
	public String getThing_routeNum() {
		return thing_routeNum;
	}
	public void setThing_routeNum(String thing_routeNum) {
		this.thing_routeNum = thing_routeNum;
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	
}
